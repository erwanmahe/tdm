<h2>jeudi 24 avril</h2>
Je reviens chez Mireya, qui ne s'attendait pas a me voir si tot.<p>
<h2>vendredi 25 avril</h2>
Je prends un gros coup de soleil/coup de bambou. Le soir, le discours de Fidel Castro est diffuse sur toutes les radios, c'est l'epoque des proces d'opposants et de l'execution des pirates de l'air. En tout cas, tout le monde s'en fout et vaque a ses occupations en esperant que la musique reprendra bientot.<p>
<h2>samedi 26 avril</h2>
Je suis cloue a la casa, tourista. La nuit , orages et pannes de courant.<p>
<h2>dimanche 27 avril</h2>
Dejeuner cubain, haricots noirs et morrceaux de porc (un peu comme la feijoada bresilienne), plus du riz avec des perritos (=perro caliente) et de la salade. En dessert, une patate douce ecrasee, melangee avec du lait et du sucre, arrosee de cannelle, c'est delicieux. Ca me change de la nourriture de la rue.<p>
Mireya m'appelle un taxi, qui monte pour negocier la course. Il me donne rendez-vous a 18h. Je quitte Cuba avec plein de souvenirs et au moins 3 personnes a qui je vais devoir ecrire une carte.<p>
<h2>lundi 28 avril</h2>
Arrivee a Santiago du Chili. Quelques courses pendant la journee.<p>
<h2>mardi 29 avril</h2>
Depart pour l'aeroport. On change de continent.<p>
