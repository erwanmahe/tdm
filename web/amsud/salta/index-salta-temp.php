<h2>7 fevrier</h2>
Nous allons directement a l'auberge de jeunesse IncaHuasi, car Tom en a eu de bons echos. C'esta 5 minutes de la gare routiere, et accueillant. Dans la chambre, il y a seulement un Israelien qui rale parce le linge qu'il a donne a laver n'est toujours pas sec apres deux jours. Il nous conseille le restaurant "El Viejo Jack" car la viande y est delicieuseet les prix imbattables. Vu la reputation des Israeliens pour degotter les bonnes affaires, Tom et moi decidons d'y aller le soir meme. Effectivement, c'est delicieux. C'est le jour et la nuit avec les restaurants cheap au Perou et en Bolivie. Nous allons ensuite tranquillement dans un quartier anime, avec un marche artisanal sur le trottoir, et plusieurs restaurants et bars. C'est la que se retrouve la jeunesse de Salta le samedi soir.
Nous trouvons notre bonheur apres 5 minutes de tergiversations. C'est un vaste endroit, avec des ecrans de tele partout et surtout des tablees entieres de filles plus belles les unes que les autres. Tout le monde est absorbe par la fin du match Boca-River. River mene 3-2 mais Boca marque a la derniere minute, puis l'emprorte aux penaltys. Hurlements de joie, Boca est l'equipe la plus populaire d'Argentine. Le voyage nous a fatigues, nous rentrons peu apres.
<h2>8 fevrier</h2>
Je me promene en ville, et je fais la connaissance
de 2 types, Carlos et Fernando, tres sympa, on va manger ensemble.
Bon repas avec une bouteille de vin. Une 2eme bouteille de vin. Il fait chaud. Nous allons ensemble dans un bar. Une 1/2 bout. de vin, puis une autre.
Arrive la, Carlos nous lache, et je reponds positivement a l'invitation charmante de Fernando: nous irons voir sa famille.
4 charmants bambins, dans une petite maison en pleine banlieue. La, pour toute personne normalement constituee, c'est le gros warning ROUGE.
Pas pour moi. Ni mes 2-2.5 grammes d'alcool.
On passe ensuite dans une autre baraque ou on achete de la biere (pour lui) et du coca (pour moi), puis je manifeste mon desir de retourner dans le centre ville.
Fernando se propose de m'accompagner, et nous voila caminando, vers 7h du soir (il fait encore bien jour), en pleine banlieue chaude de Salta. Nous devons traverser un rio asseche.
Tout d'un coup, 4 gusses surgissent des buissons et se jettent sur moi. Ils me frappent au visage et sur le crane, avec des pierres dans les mains. Pendant ce temps, je suis deleste de mon sac, qui contient mon appareil photo numerique avec les disquettes des photos de La Paz et Uyuni, une paire de lunettes de vue avec verres teintes et le Lonely Planet, et de la jolie somme de 250 pesos (1 pesos = 1/3 de dollar environ).
Une minute plus tard (ou deux, ou dix), je me releve en sang et les villageois arrivent a la rescousse. Les policiers arrivent, je monte dans le pickup avec deux marmules armes d'un fusil a pompe. La nuit commence a tomber, ils ont l'air nerveux. Qu'est-ce que je suis alle faire dans ce quartier? Une ambulance me transporte du commissariat aux urgences.
Golpeado y robo. Avec un petit trou dans la tete et la joue droite comme un hematome.
5 radios plus tard, je peux enfin rentrer et me laver, en jurant, mais un peu tard, que jamais plus on ne m'y reprendrait.
Note que je voulais voir le vrai monde, et bien je suis servi: bienvenue!
Bon, relativisons, j'avais paye l'appareil 1500 FF d'occase et les lunettes teintees me sont peu utiles car j'utilise surtout les lentilles de contact.
Physiquement, rien de casse, mais un visage qui fait peur: meme de 3/4 face, on dirait que je suis de face!
De retour a l'AJ, Tom me reconforte et je vais dormir. Je me sens pas tres en forme.
<h2>9 fevrier</h2>
Je porte plainte dans la matinee, puis j'entame une cure de television. L'AJ est equipee du cable et de canapes confortables, et je me retrouve a regarder des films toute la journee avec le personnel de l'AJ.
Je me promene de temps en temps seul ou avec Tom. Bref, je me repose.
<h2>10,11 fevrier</h2>
Idem, Tom quitte Salta pour le sud, direction Bariloche.
<h2>12 fevrier</h2>
Idem. Bus pour Mendoza a 20h.

En tous cas, je n'ai pas rencontre de voleurs.
Je vois surtout les touristes locaux, des Peruviens, Chiliens, et les Argentins d'origines diverses qui font tourner l'auberge de jeunesse.
Quoi qu'il en soit, je me balade la journee, et quand je rentre ils sont la, a picoler.
De vrais potes, quoi! On boit du vin de Mendoza (tres bon), de la biere (de la pisse comme toutes les bieres locales, du Perou a l'Argentine, mais bon on ne va pas trop leur en demander non plus, hein?) et surtout: du machin.
Le machin, c'est une mixture inventee et dosee par un Francais qui entame sa HUITIEME annee d'etudes au Chili.
Et apres ca, il veut faire un doctorat en France.
Moi qui pensais avoir le record, c'est rate.
Alors, le machin de notre ami chimiste, c'est un melange vin-coca dont les proportions sont gardees secretes dans un chateau en Espagne.
C'est bizarre, mais c'est frais et c'est de leur age, donc c'est de mon age, non ?

Voila pour Mendoza, ou j'ai passe le plus clair de mon temps a glandouiller et a degonfler (je ne sais pas pourquoi, ces deux mots ne vont pas bien ensemble, pourtant ce sont 2 activites tres differentes).

De la, direction Santiago du Chili en bus. La plus belle route que j'ai faite de ma vie.
Montagnes (Acacon-Agacon-Agagonca-conga-je sais plus, bref le plus haut sommet d'Amerique latine), canyon, temps superbe, et je n'ai plus d'appareil photo.
Le monde est cruel.

Le lendemain, hop, a Sao Paulo au Bresil. Ici, je suis dans un hotel en plein coeur de la ville, Praca de la Republica.
Je patauge completement a cause de leur putain d'accent. Comment c'est pas une accent, c'est une autre langue ?
Bref, je ne comprends rien a ce qu'ils me racontent, mais ils arrivent a suivre mon espagnol de cuisine.
Je ris encore en pensant a leur tete, a la suite d'une longue explication, quand ils devinent a mon air ahuri que je n'ai rien compris, et qu'il faut tout reprendre depuis le debut.
Ah les pauvres.

La ville est immense, et je pese mes mots, je marche tout le temps, et on ne voit quasiment plus rien de mon souvenir de Salta.
Aujourd'hui, je prends le bus pour la cote. Premiere plage depuis le debut du voyage, je sens que je vais feter ca.

Au fait, je n'irai pas au Carnaval de Rio, mais a celui de Bahia.
Il parait que c'est beaucoup mieux, plus sympa, et tout.

Je raconterai tout ca sur le site des que j'aurai le courage de m'y remettre.

C'etait Erwan a Sao Paulo, il fait 27 degres, l'air est lourd, indice de pollution 7/10, excitation pre-carnavalesque 8, intestins 7, foie 4.


A+

