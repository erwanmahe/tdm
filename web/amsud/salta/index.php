<h2>7 fevrier</h2>
Nous allons directement a l'auberge de jeunesse IncaHuasi, car Tom en a eu de bons echos. C'est a 5 minutes de la gare routiere, et accueillant. Dans la chambre, il y a deja un Israelien. Il nous conseille le restaurant "El Viejo Jack" car la viande y est delicieuse et les prix imbattables. Vu la reputation des Israeliens pour degotter les bonnes affaires, Tom et moi decidons d'y aller le soir meme. Effectivement, c'est delicieux. C'est le jour et la nuit avec les restaurants cheap au Perou et en Bolivie.<br>
Nous allons ensuite tranquillement dans un quartier anime, avec un marche artisanal sur le trottoir, et plusieurs restaurants et bars. C'est la que se retrouve la jeunesse de Salta le samedi soir.
Nous trouvons notre bonheur apres 5 minutes de tergiversations. C'est un vaste endroit, avec des ecrans de tele partout et surtout des tablees entieres de filles plus belles les unes que les autres. Tout le monde est absorbe par la fin du match Boca-River. River mene 3-2 mais Boca marque a la derniere minute, puis l'emprorte aux penaltys. Hurlements de joie, Boca est l'equipe la plus populaire d'Argentine. Le voyage nous a fatigues, nous rentrons peu apres.
<h2>8 fevrier</h2>
Je me promene en ville, et je fais la connaissance
de 2 types, Carlos et Fernando, tres sympa, ils me proposent d'aller manger ensemble.<br>
Bon repas avec une bouteille de vin. Une 2eme bouteille de vin. Il fait chaud. Nous allons ensemble dans un bar. Une 1/2 bout. de vin, puis une autre.
Arrive la, Carlos nous lache, et je reponds positivement a l'invitation charmante de Fernando: nous irons voir sa famille.
4 charmants bambins, dans une petite maison en pleine banlieue. La, pour toute personne normalement constituee, c'est le gros warning ROUGE.
Pas pour moi. Ni mes 2-2.5 grammes d'alcool.<br>
On passe ensuite dans une autre baraque ou on achete de la biere (pour lui) et du coca (pour moi), puis je manifeste mon desir de retourner dans le centre ville.
Fernando se propose de m'accompagner, et nous voila caminando, vers 7h du soir (il fait encore bien jour), en pleine banlieue chaude de Salta. Nous devons traverser un rio asseche.
Tout d'un coup, 4 gusses surgissent des buissons et se jettent sur moi. Ils me frappent au visage et sur le crane, avec des pierres dans les mains. Pendant ce temps, je suis deleste de mon sac, qui contient mon appareil photo numerique avec les disquettes des photos de La Paz et Uyuni, une paire de lunettes de vue avec verres teintes et le Lonely Planet, et de la jolie somme de 250 pesos (1 pesos = 1/3 de dollar environ).<br>
Une minute plus tard (ou deux, ou dix), je me releve en sang et les villageois arrivent a la rescousse. Les policiers arrivent, je monte dans le pickup avec deux marmules armes d'un fusil a pompe. La nuit commence a tomber, ils ont l'air nerveux. Qu'est-ce que je suis alle faire dans ce quartier? Une ambulance me transporte du commissariat aux urgences.
Golpeado y robo. Avec un petit trou dans la tete et la joue droite comme un hematome.<br>
5 radios plus tard, je peux enfin rentrer et me laver, en jurant, mais un peu tard, que jamais plus on ne m'y reprendrait.
Note que je voulais voir le vrai monde, et bien je suis servi: bienvenue!<br>
Bon, relativisons, j'avais paye l'appareil 1500 FF d'occase et les lunettes teintees me sont peu utiles car j'utilise surtout les lentilles de contact.<br>
Physiquement, rien de casse, mais un visage qui fait peur: meme de 3/4 face, on dirait que je suis de face!<br>
De retour a l'AJ, Tom me reconforte et je vais dormir. Je me sens pas tres en forme.
<h2>9 fevrier</h2>
Je porte plainte dans la matinee, puis j'entame une cure de television. L'AJ est equipee du cable et de canapes confortables, et je me retrouve a regarder des films toute la journee avec le personnel de l'AJ.
Je me promene de temps en temps seul ou avec Tom. Bref, je me repose.
<h2>10 fevrier</h2>
Idem, Tom quitte Salta pour le sud, direction Bariloche.
