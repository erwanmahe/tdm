<h2>12 fevrier</h2>
J'arrive a Mendoza en fin de matinee. La ville est asez grande, mais elle ressemble plus a un lieu de vacances que Salta.<br>
Je prends le trolley-bus jusqu'a l'AJ que me conseille l'office du tourisme au terminal de bus. C'est en face du "parque", magnifique.<br>
L'ambiance de l'AJ est super-friendly, je passe du temps avec les touristes locaux, des Peruviens, Chiliens, et les Argentins d'origines diverses qui font tourner l'auberge de jeunesse.<br>
Le soir, je revois tout le monde. Nous parlons espagnol, enfin plutot argentin, et nous buvons du vin de Mendoza (tres bon), de la biere (tres legere comme toutes les bieres locales, du Perou a l'Argentine) et surtout: du machin.<br>
Le machin, c'est une mixture inventee et dosee par un Francais qui entame sa HUITIEME annee d'etudes au Chili.
Et apres ca, il veut faire un doctorat en France.
Moi qui pensais avoir le record, c'est rate.<br>
Alors, le machin de notre ami chimiste, c'est un melange vin-coca dont les proportions sont gardees secretes dans un chateau en Espagne.
C'est bizarre, mais c'est frais et c'est de leur age, donc c'est de mon age, non?<br>
<h2>13 fevrier</h2>
Je reserve le billet d'avion de Santiago du Chili a Sao Paulo le dimanche 16 fevrier.<br>
Le soir, je m'offre une parillada dans un restaurant, "El Balcon". Tout est divin: cotelettes, chorizo, boudin noir et surtout les deux morceaux de viande qui ne contiennent presque du gras, je pense les laisser mais au final c'est ce qu'il y a de meilleur. Apparemment, la parillada est un plat pour deux. Tant pis ;-)
<h2>14 fevrier</h2>
Ballade en ville, le temps est superbe, la chaleur supportable. Le soir, je retrouve les autres. ils ont un plan pour une soiree a l'exterieur de la ville. Nous allons prendre les informations dans un bar a cote, puis nous suivons un gars qui va nous emmener a la fete. Il a un pickup, 3 personnes a l'avant, 6 a l'arriere. Je suis a l'arriere, of course. La route est plus longue que prevu. les 5 minutes se transforment en 15-20 minutes par le parc, puis la rocade. Nous roulons vite. Je me force a ne pas penser au taux d'alcoolemie probable du chauffeur. Arrives sur les lieux, nous sommes apparemment les premiers et l'ambiance ne decolle pas apres deux heures. Nous rentrons en taxi a l'AJ.
<h2>15 fevrier</h2>
Apres des adieux dechirants, je prends le bus pour Santiago.<br>
La route est superbe: un canyon, entoure de montagnes tres hautes, puis un col eleve (le poste de douane "Los Liberatores" est quand meme a 4000 metres!). L'Aconcagua n'est pas tres loin. C'est surement (a ce jour) la plus belle route que j'ai faite de ma vie.
Le temps aussi est magnifique, et je n'ai plus d'appareil photo.<br>
Le monde est cruel.<br>
Le chauffeur de bus est assez marrant, physiquement il ressemble a Danny de Vito. Nuit d'hotel a 8000 pesos (a peu pres 80 FF), ca me fait un choc maintenant que je suis bien habitue aux tarifs argentins (l'AJ de Mendoza m'a coute 10 pesos, soit environ 20 FF).
<h2>16 fevrier</h2>
A l'aeroport, apres negociations pour ne pas payer les taxes d'aeroport: "si, je vous jure, j'ai deja paye les taxes sur tous les vols, c'est ecrit sur le billet" en espagnol approximatif, en faisant de grands gestes. Le personnel est stoique mais courtois. Tout s'arrange finalement et je monte a bord d'un A340, equipe d'ecrans de television pour chaque passager. Le luxe, quoi!

