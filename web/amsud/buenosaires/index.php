<h2>7 avril</h2>
Arrivee a Buenos Aires a 7 heures du matin. Je pose mon gros sac a la consigne, et je dois faire le tour du Retiro (gare + gare routiere) pour trouver l'arret du bus 130, direction Recoleta. Je n'ai pas de petite monnaie pour payer, mais un passager me fait du change. Le temps de payer et de m'asseoir, je suis deja arrive. J'aurais aussi vite fait a pied! C'est le quartier des grandes avenues, des grands hotels et des grands cimetieres. Apparemment, c'est l'heure du pipi canin: je vois plusieurs personnes se promener tout autour des jardins publics, tenant en laisse une meute de superbes chiens de race.<p>
Apres une halte petit-dejeuner bien meritee, je me teleporte a San Telmo, je loue une chambre a la "Residential Carly" et vais recuperer mon sac a la consigne. L'apres-midi, je me promene dans le centre puis Puero Madero, un quartier tres moderne construit autour du canal. Le soir, je dine au Paraiso, d'un "tiro asado". Toujours cette impression que les plats sont en fait dimensionnes pour deux personnes. Apres ca, impossible de bouger, retour casa moitie en marchant, moitie en roulant.<p>
<h2>8 avril</h2>
Je passe la journee a batifoler dans San Telmo, c'est plein de petites rues, d'antiquaires, de vieux immeubles. Il fait beau, mais il y a du vent. Le midi je tombe en arret devant un restaurant: bife de lomo. Vu mon regime, je prends facilement un kilo par jour! Mais bon, je me pardonne.<p>
L'apres-midi, je me lance dans la quete d'un scanner pour les photos que je viens d'envoyer a faire developper. Tout San Telmo me connait, j'en suis sur, comme le gringo au scanner. Mission accomplie, apres 2 heures de tours et detours au milieu des theatres, boutiques d'antiquites et cinemas d'avant-garde, plus les universites et ecoles d'art qui foisonnent dans ce quartier. J'ai aussi repere un tas de petits restaurants, bien sur!
<h2>9-13 avril</h2>
Il fait beau, mais il y a de plus en plus de vent. Je continue mes promenades dans la ville, comme si de rien n'etait. Samedi soir, sortie dans une boite, dont je ne me rappelle plus le nom. C'est un site immense, apparemment un ancien theatre, sur 4 etages, avec des colonnades partout et des lustres tous les 15 metres (c'est VRAIMENT immense), le tout sur fond de techno deluree, bref ambiance decadente a souhait pour la jeunesse argentine branchee.
Le dimanche, j'assiste a une demonstration de tango sur la place ??, juste en face de mon hotel.
<h2>14 avril</h2>
Avion pour Santiago.<p>

