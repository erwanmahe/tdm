<h2>vendredi 21 novembre</h2>
L'obtention du visa, decrite comme une procedure longue et [enible dans tous les guides que j'ai pu avoir en main, se deroule en mode express. Un douanier tamponne mon passeport, le passe a un autre douanier qui m'accompagne jusqu'a un distributeur automatique (je n'avais pas non plus de liquide), je prends pour 50$ de Schillings kenyans, les lui donne, et "merci monsieur, bon sejour au Kenya". 3 minutes trente. Wouaouh.<p>
Des l'aeroport, je suis harcele par les "stouts", ce sont les nombreux Kenyans qui vivent de commission aupres des hotels, des organisateurs de safaris, etc... L'un d'entre eux m'accompagne en centre ville, grace a lui je ne paie que 100 KSh, c'est une affaire. En contrepartie je le suis dans les bureaux de 2 organisateurs de safaris, dont le Primetime Safaris, ou je rencontre un couple de Canadiens qui s'appretent a partir en safari.<p>
Soiree au Nairobi Youth Hostel, je rencontre des etudiants Kenyans, on joue au scrabble(!) en anglais, je finis 2eme sur 4. Pas si mal. Ils me font gouter l'ugali, specialite locale.<p>
J'apprends que beaucoup de mots et de noms du "roi lion" sont en fait du swahili: "hakuna matata"="pas de probleme, simba=lion, jambo=bonjour...<p>
Je reste discuter tard avec 2 d'entre eux, plus Ali, qui a travaille aux Etats-Unis dans l'informatique. Donc il est au chomage ;-) et il travaille maintenant dans l'industrie du tourisme, en gros c'est un "tout" lui aussi.<p>

<h2>samedi 22 novembre</h2>
Je passe la journee en ville, l'auberge est assez loin du centre malheureusement. Diner au Carnivore (restaurant ultra-connu a Nairobi, ou l'on mange toutes sortes d'animaux sauvages, cuits au barbecue. C'est delicieux, mais cher, d'ailleurs il n'y a pour ainsi dire que des blancs) avec Marc (NL), James (AUS) et Tom (US).<p>

<h2>dimanche 23 novembre</h2>
Retour Primetime, je m'inscris pour un safari de 3 jours (180$ pour 3 jours au Masai Mara). Je demenage dans les locaux de Primetime, car ils offrent 3 jours dans leur GH a quiconque leur commande un safari. En plus c'est en plein centre ville.<p>

<h2>lundi 24 novembre</h2>
Probleme de carte bleue, il semble que j'aie explose le plafond de retrait en distributeur. Je passe par l'internet/email (pas assez rapide), le telephone par IP (mauvaise communication, avec un modem), la banque Barclays (1 heure et demie de queue, sans resultat), le telephone satellite (hors de prix), et finalement la solution: cash advance de la part de Primetime.<p>
Apres cette journee angoissante, je revois les Canadiens, ravis de leur safari.<p>

<h2>mardi 25 novembre</h2>
Debut du safari, avec Samy le driver (KEN), et les sympathiques touristes Harry (GB), Stephen et Michele (CH). 1er "game drive" (avec le minibus dans le parc, appareil photo en main). La reserve (ou parc) est immense, les animaux sont en liberte. Genial, plein de photos de guepards (une mere et son petit) et de lions (un couple exhibitionniste). Meme une photo d'un troupeau de Nissans, assez courants depuis qq annees.<p>
Le soir, nous arrivons au campement. C'est le luxe, chaque tente (canadienne) est recouverte d'un toit en paille, et contient deux vrais lits. Le luxe. La douche au fond du camp est chauffee, mais chacun apporte sa lumiere.<p>

<h2>mercredi 26 novembre</h2>
Game drive toute la journee, encore mieux qu'hier. Elephants, girafes, zebres, buffles, zebus, lions, guepards, phacocheres, babouins, aigles, vautours, crocodiles et hippopotames. Et meme un rhinoceros noir (tres rare, parait-il). Le seul qui  echappe a notre palmares est le leopard, jusque la indetectable.<p>
Le soir, nous visitons un village masai, pour 500 KSh chacun. Ca fait un petit peu "fake", mais on apprend qq trucs interessants sur la vie de ce village (surtout sur leur vie il y a qq annees, avant le tourisme de masse).<p>

<h2>jeudi 27 novembre</h2>
Depart tot pour le dernier "game drive", avant le petit dejeuner. Nous cherchns un leopard, le dernier des "big five" qui nous ait echappe. Les Big 5 sont: Le lion, l'elephant, le rhinoceros, le buffle et le leopard.<p>
Nous apercevons un royal lion, assis sur son royal tertre, observant de loin "ses" buffles en lechant ses royales babines. C'est le prototype du lion. Il nous ignore superbement, il a appris depuis longtemps que le Nissan, ca ne se mange pas, ca ne mange pas les lions ni la nourriture des lions, bref aucun interet. Nous apercevons par chance un enorme python a deux pas du minibus. Mais point de leopard.<p>
Le retour a Nairobi est laborieux, nous devons attendre un autre minibus qui doit emmener les Suisses au lac, et Harry dans une auberge ou il a laisse un velo: il veut continuer par ses propres moyens apparemment...<p>

<h2>vendredi 28 novembre</h2>
Je prends mon ticket pour Mombasa le samedi matin. Je passe la soiree a discuter a l'"hotel" primetime avec un Japonais qui est la depuis le debut, apparemment, il mange, dort, et lit le plus clair de son temps. Il semble avoir adopte la maison. Soiree video avec "Masai", le masai qui garde les bureaux/hotel la nuit. Il m'a dit son vrai nom une fois, 4 syllabes. De toutes facons tout le monde l'appelle Masai.<p>

<h2>samedi 29 novembre</h2>
Bus 8h30, le chauffeur fait la chasse aux matatus (minibus concurrencant des liaisons regulieres par bus). La route est ... africaine.<p>

