<h2>jeudi 28 aout</h2>

Des notre arrivee, nous prenons une chambre au Flower Inn, tres jolie auberge (avec seulement 2 chambres) avec un jardin tropical. Ballade autour du rocher, la chaleur est ecrasante. Nous prenons le repas du soir a l'auberge, un mix de specialites locales. A la nuit tombee, nous affrontons la nuit et les elephants sauvages pour aller boire un verre dans un hotel de luxe non loin de la.<p>

<h2>vendredi 29 aout</h2>

Nous escaladons le rocher dans la matinee, la vue de la-haut est superbe: c'est un rocher carre plante au beau milieu d'une jungle immense. Au sommet du rocher, les ruines d'une forteresse qui a toute une histoire: le fils d'un roi assassina son pere et expedia son frere en exil pour s'approprier le trone. Par peur des represailles, il fit construire une forteresse inexpugnable au sommet de ce rocher. Mais a la fin de l'histoire, le fils exile revient et venge son pere.<p>

Hanneke s'en va pour Passekundun, je vais a Trincomalee, un peu plus au nord. Je prends un tuk-tuk pour Dambulla, nous nous arretons en route pour faire de l'essence. L'unite de mesure est la bouteille de coca! J'attends mon bus devant un stand de loterie d'un gars equipe d'un micro, qui passe ses journees a haranguer la foule. Les deux lots principaux, une moto et une voiture, sont bien en evidence en plein milieu du stand. C'est une loterie privee: il a achete les lots, a fait faire une serie de tickets de loterie et c'est parti! Il me renseigne sur les bus qui arrivent, car j'ai des problemes avec les destinations, ecrites en caracteres sri lankais. Le bus arrive enfin, mais je suis serre, debout, a moitie appuye sur mon sac, tres inconfortable. Nous arrivons a Trincomalee 2h30 plus tard, la nuit tombe.<p>

