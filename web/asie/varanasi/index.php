<h2>lundi 22 septembre</h2>

J'arrive a Varanasi le matin, j'attends l'ouverture de l'office du tourisme. Un taxi (systeme pre-paid) se propose de m'y ammener. Le temps de sortir du parking, le taxi a change deux fois de chauffeur et le tarif a ete multiplie par deux. Je sors, prends un tuk-tuk, pre-paid aussi. Et je suis enfin a la Maruti GH. Je passe le debut d'apres-midi a faire la sieste, les coupures d'eau et d'electricite n'arretent pas.<p>

<h2>mardi 23 septembre</h2>

Promenade en ville, elle est sale, encore plus que Chennai.<p>
Les rues etroites sont envahies de motos, rickshaws, pietons et vaches. Le long de la rue principale coule le Gange, au moins aussi sale que la ville. Bref, j'aime pas, mais alors pas du tout.<p>

<h2>mercredi 24 septembre</h2>

La promenade en bateau que j'avais arrangee pour ce matin tombe a l'eau (ha ha) a cause de la pluie. Je me perds dans le souk, visite d'un atelier de confection de soie. Et j'achete quelques articles en soie et en pashmina, sans rien y connaitre.<p>

<h2>jeudi 25 septembre</h2>

Bus pour Katmandou, en fait un 4x4 pour 10 personnes: le chauffeur (Indien), Fred (Francais), Petra et son pere (Suisses) et Orwen + 3 amis(Israeliens). Bref, l'ONU en vadrouille.<p>
La route est completement defoncee, nous crevons un pneu. Nous sommes souvent arretes par la police qui recherche des activistes maoistes.<p>
Nous arrivons a la frontiere en retard, le chauffeur bombe malgre l'etat de la route. Il mache du tabac pour ne pas s'endormir, il a la bouche pleine d'un jus noiratre qui l'empeche d'articuler. Autant dire que personne ne comprend ce qu'il dit. <p>
Nous arrivons a la frontiere a la tombee de la nuit, il pleut et les rues sont noires de monde. Apres le parcours du combattant aux deux postes frontieres, notre petit groupe se dirige vers la Nepal GH, ou nous passons la nuit dans un confort spartiate.<p>

<h2>vendredi 26 septembre</h2>

Depart 8h30, un autre 4x4 nous depose a la gare, ou nous montons enfin dans un vrai bus avec de la place pour tout le monde. Du groupe initial il ne reste que Fred, les Israeliens sont partis tres tot ce matin et les Suisses ne vont pas a Katmandou mais dans un parc naturel.<p>
La 1ere partie de la route est pourrie, puis ca s'ameliore puis la route devient franchement pourrie voire dangereuse: nous roulons sur une corniche ecroulee par endroits, a ma droite d'enormes blocs de pierre qui ne demandent quï¿½a se detacher, a ma gauche la riviere (beaucoup plus bas a ma gauche). Cela dit, le paysage est magnifique. Nous sommes bloques dans une file immense de vehicules et pendant 4-5h, nous avancons au pas.<p>
Nous arrivons enfin vers minuit a Katmandou, on dirait une ville sous controle militaire: nous apprenons que le chef de la police de Katmandou vient de se faire abattre la veille. Ca promet.<p>

