<h2>vendredi 26 septembre</h2>

A la sortie du bus, tout est desert, sauf.. un groupe de jeunes qui nous attendaient apparemment depuis un bon moment et nous proposent leur hotel, a un prix defiant toute concurrence (selon eux), mais nous, c'est-a-dire Fred, Nico (Grec) ainsi que Violaine (Belge) et son ami Promod (Nepalais), et moi, decidons de dormir dans le bus, jusqu'a 5h, heure a laquelle le chauffeur doit repartir vers la frontiere. Nous montons dans 2 taxis, direction Freak Street (j'adore le nom).<p>

<h2>samedi 27 septembre</h2>

Petit dejeuner sur la terrasse de l'Eden GH, puis nous demenageons pour Le Traveller's Paradise, toujours dans Freak St. Nous allons nous promener en ville, le temps est superbe, nous oublions la fatigue du voyage.<p>

<h2>dimanche 28 septembre</h2>

Re-promenade, cette fois nous sommes bardes de guides et de cartes, nous visitons les monuments de la place principale, puis marchons jusqu'au Monkey Temple (ainsi nomme parce qu'il est habite par de nombreux singes), un peu en marge de la ville. L'escalier qui mene au temple a du en decourager plus d'un. Mais l'ascension est recompensee par l'ambiance mystique (les moulins a priere, les moines, les statues) et decontracte (les singes qui font les cons et se glissent partout) du "sommet".<p>

<h2>lundi 29 septembre</h2>

Lever 5h, je reveille Fred, nous sommes partis pour un trek de 3 jours avec Toya, un guide que nous a presente Promod. Dans le bus, Toya m'explique les dieux hindous, l'origine du Bouddha et comment les hindous l'ont integre dans leur propre systeme de croyances. Il fait des etudes de litterature anglaise et se destine au journalisme. Dans une monarchie a tendance dictatoriale, je lui souhaite bien du courage. Mais pour lui, tout est beau, il aime son roi car les journalistes ont le droit de le critiquer. Nous arrivons a Daktapur, ville fortifiee tres ancienne. Nous nous glissons a l'interieur de la ville sans payer le ticket d'entree. C'est amusant de voir que l'architecture moyen-ageuse est la meme partout dans le monde. D'un autre cote, je ne sais meme pas de quand date leur moyen-age, ni ca s'appelait comme ca. Mais certaines parties des constructions sont tres similaires a ce qu'on trouve en Europe.<p>
Nous prenons un bus jusqu'a Changu pour le petit dejeuner en terrasse. Sur la place, jsute devant le guichet du ministere du tourisme (decidemment), des enfants font voler des cerfs-volants, c'est un jeu tres populaire ici.<p>
De la, nous commencons le trek proprement dit, et nous arrivons a Nagarkot un peu moins de 4h plus tard, apres une pause the a Telkot, ou je rencontre un directeur d'ecole qui m'aide a dechiffrer les affiches placardees sur les murs. Les paysages sont magnifiques, mais nospoumons ne profitent pas trop de l'air pur: de nous trois, seul Fred a decide de ralentir sur la cigarette, Toya et moi profitons de la moindre occason pour en griller une.<p>

A Nagarkot, petit village assez frisquet au sommet de la colline, nous retombons sur Orwen qui nous presente Leila, une Israelienne tres mignonne avec qui Fred et moi discutons longuement. Orwen commence a regretter son geste et ils nous quittent assez abruptement. Grrr
Nous sommes sur un terre-plein herbeux, un groupe est agglutine autour d'une grande balancoire, les candidats au "balancage" s'elevent facilement a 5-6 metres de hauteur. Les gamins montent a deux, tout le monde crie et s'amuse. Derriere, l'Himalaya enneige et ennnuage, imperturbable.<p>
Nous trouvons un bungalow avec vue sur la brume qui recouvre l'Himalaya, Madhuban Bamboo Cottage, la aussi un plan Promod. La situation est geniale, nous sommes face aux montagnes, pas un bruit, la serenite absolue. Une heure plus tard, un groupe de 6 Israeliens braillards debarque. Tant pis pour le calme.<p>

<h2>mardi 30 septembre</h2>

Apres une marche jusqu'a Sankhu, nous prenons le bus jusqu'a Sunarijal, ou nous sommes accueillis par la pluie. Nous devons grimper un escalier interminable qui remonte la riviere en furie sur notre droite. Nous arrivonsau lac completement casses et assez decus. Il faut dire que l'espece de mare equipee d'un barrage hjydroelectrique datant du siecle dernier ne correspond pas a l'idee que nous nous faisions d'un lac.<p>
Nous sommes censes monter en suite jusqu'a Chisopani, mais nous supplions Toya de s'arreter a la premiere GH, ou nous nous ecroulons. Apres toute cette boue et cette grimpette epuisante, je suis tres sale, dï¿½un autre cote, la douche est glacee: cas de conscience.<p>

Nous partageons le repas avec Zuzana, une touriste tcheque mignonne, un peu allumee, qui vit en Belgique dans une roulotte, dans un quartier "autonome", je n'ai pas tres bien compris le concept mais je me suis promis d'aller voir.<p>

<h2>mercredi 1er octobre</h2>

Fred a mal aux pieds, je finis donc le trek avec Tuya jusqu'a Chisopani, 3 heures de montee, avec les porteurs (sherpas) de boisson, de nourriture, d'essence, etc. Nous sommes presque arrives au sommet, la brume recouvre tout en un instant. La vue avait l'air superbe pourtant. Nous faisons la pause la-haut, le temps de regarder arriver un groupe de 2 occidentales et 3 porteurs nepalais s'installer. En fait, ce sont les Nepalais qui installent le camp pendant que les occidentales font bronzette tiens, le soleil est revenu). La descente dure 2 heures et demie, et nous retrouvons Fred et Zuzana pour le dejeuner.<p>

Le retour en bus est laborieux, nous utilisons plusieurs bus avant d'arriver a Katmandou, creves (sauf moi qui ai la mega-peche, je ne sais pas pourquoi)<p>

<h2>jeudi 2 octobre</h2>

Je me reveille avec une creve d'enfer, le patron de la GH me prepare un grog (au whisky, ils n'ont pas de rhum par ici), puis un autre, mais rien n'y fait.<p>

<h2>vendredi 3, samedi 4 octobre</h2>

J'ai arrete le traitement a base de grog avant la cirrhose, et je suis passe a plus traditionnel. Les medicaments achetes a l'unite chez le pharmacien n'ont pas beaucoup plus d'effet que le rhum, mais avec le temps je peux sortir plus longtemps de ma chambre.<p>

<h2>dimanche 5 octobre</h2>
J'accompagne Fred et Violaine sur la place --, c'est le jour du festival --, nous assistons au sacrifice rituel de quelques vaches.<p>

<h2>lundi 6 au jeudi 10 octobre</h2>
Retour Bombay par la Nepal Airlines.<p>
A Bombay, retour a l'Apollo GH, je visite la ville et fais quelques achats. Je modifie mon billet d'avion avec la Cathay Pacific qui en profite pour me facturer toout un tas de services. Je retourne au cinema, pour l'ambiance, tout le monde se leve au debut de la seance pour ecouter l'hymne national. C'est le seul moment de la seance opendant lequel les Indiens sont calmes et disciplines.<p>
Mon avion se pose a Bangkok le 9. J'obtiens un vol pour Paris en un temps record, encore Vietnam Airlines. Je fais mon dernier tour a Panthip Plaza, un dernier tour de la ville et je m'envole pour Paris le 11, apres avoir paye un "excess baggage" de 250$, soit le prix de mon billet aller pour Paris.<p>

<h2>samedi 11 octobre au 9 novembre</h2>
Paris.<p>

<h2>dimanche 10 novembre</h2>
Arrivee Bangkok, je prends mon billet et je decolle pour Hong-Kong, sans meme sortir de l'aeroport.<p>
Nuit dans l'aeroport de HK, immense et tres inconfortable.<p>

<h2>lundi 11 novembre</h2>
Montee en puissance de l'aeroport jusqu'au depart de mon vol pour Den Pasar.<p>

