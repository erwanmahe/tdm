<h2>dimanche 10 aout</h2>
Nous arrivons sur la place du marche, immense, et un mototaxi me depose gratuitement (contre une commission je suppose) dans une guest house pres du lac. Il m'emmene ensuite dans une agence de voyage ou je peux, moyennant 5$ de commission, retirer 100$ avec ma carte visa: je suis riche! Un e-mail a Leesan plus tard, je me retrouve sur mon lit, plein aux as et la galere est loin. Je dois encore un service a Leesan, mais ca attendra demain.<p>

<h2>lundi 11 aout</h2>
Un petit tour sur internet, je refais le CV de Leesan et lui achete deux dicos de poche: khmer-anglais et anglais-khmer: j'ai decide de le bichonner, mon Leesan. Le marche, architecture coloniale, est un vrai dedale a 4 branches: a l'exterieur, les stands de nourriture, puis les vetements, enfin les bijoux et les montres sous le dome central. Je change de GH: j'habite maintenant chez Simon's Guesthouse, plus propre et surtout je ne dois pas retirer mes chaussures a chaque fois que j'entre.<p>

<h2>mardi 12 aout</h2>
Visite touristique de PP, a l'arriere d'un moto-taxi. Shooting range, ou on peut tirer avec toute sorte d'arme, du M16 a l'AK47 en passant par le FAMAS et le lance-roquettes... Nous allons visiter ensuite les killing fields, a bout d'une route longue et plus que defoncee. C'est un camp de concentration de l'ere Pol Pot. Exposition de cranes et fosses communes, c'est horrible. Ensuite le chauffeur me demande s'il peut faire un crochet, et nous nous retrouvons en pleine banlieue, dans les locaux d'une ONG ou il a postule pour un poste. Mais le patron (anglais) ne se montre pas et l'entretien est repousse a la semaine suivante (je n'ai rien contre les Anglais). Au final, mon chauffeur me laisse a S-21, le QG de la police politique de Pol Pot, une prison en fait, maintenat occupee par des photos et des peintures qui perpetuent le souvenir de la souffrance et des tortures.<p>

<h2>mercredi 13 aout</h2>
Je passe la journee dans les rues de PP et au marche. Le soir au Flying Elephant, tenu par une travelleuse australienne. C'est son idee des "petits boulots" a l'etranger: pourquoi chercher un salaire quand on peut etre le boss? Je distribue quelques CV pour Leesan.<p>

<h2>jeudi 14 aout</h2>
Promenade le long du Mekong, visite des temples (wat), le wat phnom (sur une colline decouverte par Penh, qui fonda la ville), le palais royal, et quelques visites de restaurants-guesthouses-bureaux de tourisme pour distribuer le CV. Le soir au Heart of Darkness, ou je rencontre un expatrie anglais qui travaille a Bangkok.<p>

<h2>vendredi 15 aout</h2>
Depart 7h pour Siem Reap, la ville des temples d'Angkor. Je retrouve le couple de Vosgiens dans le bus.<p>

