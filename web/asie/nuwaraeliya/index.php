<h2>dimanche 24 aout</h2>

L'arrivee a Nuwara Eliya est triomphale, nous traversons un viaduc entre deux collines avant de nous arreter a la gare, ou nous sommes immediatement pris en charge par un taxi qui nous emmene dans l'hotel le moins cher de la ville ("parole!"). Nous trouvons un peu mieux ailleurs, et posons nos affaires. La ville est surnommee "Little England", tout y est: le champ de courses, le terrain de foot, les maisons coloniales de trois etages, en bois peint, le brouillard et meme quelques Anglais (j'ai failli dire des "locaux").<p>

<h2>lundi 25 aout</h2>
Nous partons aux aurores, direction Horton Plains, en tuk-tuk, pas vraiment adapte aux routes de montagnes. Nous nous arretons sur la route dans un boui-boui et partageons le petit dejeuner (du genre compact) avec les cultivateurs de the, les Tamouls.<p>
Ensuite, ca commence a monter vraiment. Plus nous prenons de l'altitude et plus le brouillard s'epaissit. Apres environ 2 heures de routes en lacets, nous arrivons enfin sur le plateau de Horton Plains. La ballade est agreable, nous suivons le petit chemin et nous emerveillons de la beaute de Dame Nature entre deux nappes de brouillard. La visite s'acheve par une seance de photos a world's end, le sommet d'un precipice de 3000 pieds. Des petits singes font leur apparition sur la falaise, ce qui porte le compte d'especes sauvages que j'ai vues a Horton Plains a: 2 (en comptant les ecureuils, j'ai dit sauvage, pas dangeureux).<p>

Nous redescendons sous la pluie. Pour ceux qui n'ont jamais teste la combinaison tuk-tuk aux 3 pneus lisses + pluie + pente a 15% + virage en epingle a cheveux, je dirai ceci: evitez.<p>

Nous finissons la journee par la visite d'une laiterie, ou on fabrique du lait en poudre (les fabriques de the etaient trop loin): bizarrement, c'etait la partie du voyage la plus interessante. Et nous rentrons dans l'apres-midi, retour par le train de nuit, je finis dans le wagon "cantine-stand boisson" ou regne une bonne ambiance. Nous arrivons a Kandy a 4 heures du matin, Michael rentre a Colombo, apres m'avoir dument tape 10$ pour son salaire de "guide". Apres toutes les commissions dont il a beneficie, il est quand meme gonfle.<p>
