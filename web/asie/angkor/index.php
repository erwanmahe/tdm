<h2>vendredi 15 aout</h2>
Apres plus de 8h de route en terre, nous arrivons enfin a Siem Reap, je prends une chambre a l'Orchidea. Un mototaxi m'emmene a Angkor pour le coucher du soleil. Je suis le flot des touristes qui montent le Phnom ?? a la queue-leu-leu, la pente est assez raide. Je n'en peux plus d'attendre le coucher du roi, et je descends avant la meute.<p>

<h2>samedi 16 aout</h2>
Lever 4h, je vais voir le lever du soleil sur Angkor wat. Completement endormi, comme la plupart des touristes (nettement moins nombreux qu'hier) qui se trainent a l'interieur du temple. Donc j'y suis au moment crucial. Et c'est beau. Quelques photos plus tard, je suis de nouveau sur le moto-taxi pour la visite des temples, menee a un train d'enfer: le soleil tape de plus en plus, et arrive a midi je declare forfait: on rentre.<p>
De retour a l'Orchidea, je regarde le match de foot Montpellier-Monaco avec les employes de la GH. Ils adorent le foot, mais ne connaissent pas les equipes! Pour eux, c'etait le match Teddy Smith contre Fincominvest. Je passe la fin de l'apres-midi dans un hamac, tout va bien.<p>

<h2>dimanche 17 aout</h2>
Rien de la journee, u peu d'internet, je discute avec quelques locaux. Je dine le soir dans une espece de cantine au marche, du poisson delicieux. Je revois les Vosgiens.<p>

<h2>lundi 18 aout</h2>
Je loue un velo, histoire de visiter les derniers temples. J'y passe toute la matinee, et je termine par les plus prestigieux. Je croise un serpent (un petit vert tres rapide). Le soir, massage d'une heure pratique par une aveugle: je ne suis pas tres tranquille, en tous cas au debut.<p>

<h2>mardi 19 aout</h2>
Depart Bangkok, la route est encore plus defoncee qu'entre PP et Siem Reap. Frontiere et BKK vers 18h.<p>

<h2>mercredi 20 aout</h2>
Je cherche les bureaux de la Cathay Pacific, ca se termine en promenade sur Sukhumvit Road, la rue la plus longue de la ville! Bref je me suis perdu, et j'ai aussi perdu ma journee. A Kao Sarn Road, je rencontre successivement les Belges Sebastien et Xavier, puis Mireille (la Hollandaise) qui part demain matin, comme d'habitude.<p>

<h2>jeudi 21 aout</h2>
Je vais reserver mon vol directement a l'aeroport, au moins je ne risque pas de m'y perdre. Vol le soir-meme, a 21h30. J'aurai fait 2 aller-retours Kao Sarn-aeroport, dans les embouteillages et sous la flotte. Assez pour me degouter de la ville ou j'aurai passe tellement de temps. Adieu Bangkok, bonjour Colombo.<p> 
