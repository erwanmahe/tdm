<h2>mardi 26 aout</h2>

Je me rends au "Olde Empire", pour trouver porte close. Ca me permet d'assister au lever du soleil sur le lac de Kandy, ainsi qu'au petit dejeuner des singes. Arrive le moment de l'ouverture, je recupere mon sac, reprends une chambre et me fais engueuler par le receptionniste. Il est vrai que je leur ai laisse mon sac il y a trois jours, en leur disant "a demain", il y a de quoi etre inquiet.<p>

J'achete mon billet d'avion sur Sri Lanka Airlines, la meilleure compagnie du monde depuis 3 ans, d'apres le prospectus. Visite a l'ambassade d'Inde, je repasserai demain matin avec deux photos d'identite.<p>

Je m'ecroule sur mon lit.<p>

<h2>mercredi 27 aout</h2>

Retour ambassade, j'aurai mon visa dans une semaine. La journee se passe agreablement en compagnie de quelques touristes rencontres a l'ambassade, dont Hanneke, une Hollandaise qui dirige un orphelinat en Inde. Soiree vin francais + fromage italien + toasts et tomates locales, devant le lac, en compagnie d'Hanneke. Rendez-vous est pris pour demain matin, nous allons a Sigiriya.<p>

<h2>jeudi 28 aout</h2>

Nous manquons le rendez-vous a 7h15, et apres plusieurs allers-retours entre la gare routiere et l'hotel de Hanneke, je me decide a partir, il est 10h.<p>
Le bus me depose a Dambulla, c'est un carrefour de plusieurs routes importantes, la route est bordee d'arrets de bus (ou plutot d'endroits ou les pietons ont decide d'attendre et les bus de s'arreter). Je decide a mon tour de m'integrer dans la file pour Sigiriya, ou je retrouve, O surprise, Hanneke. Apres 30 minutes serres comme des sardines, le bus nous ejecte enfin a Sigiriya.<p>
