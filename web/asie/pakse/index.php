<h2>mardi 5 aout</h2>
Je mote dans un truck (c'est un pickup-taxi), nous sommes 12 a bord. Nous traversons un pont, et on me depose en face de ma guesthouse apres environ 8 km. Ici les gares routieres sont en bordure de ville: ca fait travailler les taxis. La guesthouse est immonde, mais je n'ai pas le courage de chercher autre chose. J'herite d'une chambre au dernier etage. Je dine dans un restau indien, qui fait aussi agence de tourisme. Resultat: tous les touristes sont agglutines ici.<p>

<h2>mercredi 6 aout</h2>
Je demande a payer 1$ au lieu de 2 car:<br>
- j'ai du faire la chasse aux cafards geants pendant une heure<br>
- la chambre n'a pas ete nettoyee pendant un mois<br>
- il a plu dans la chambre en plein milieu de la nuit<br>
Le type a l'accueil etait desole, j'ai son patron au telephone qui accepte le paiement de 1$.<p>
Je fais le tour des hotels de la ville, pas une chambre a moins de 5$. Bon, je retourne a la GH, et demande une chambre au 1er etage, au moins je n'aurai pas a me preoccuper de la pluie.<p>
A la guesthouse, je fais la connaissance de Pierre (c'est son nom en France, j'ai oublie son nom lao) qui a vecu 20 ans en France. Il habite chez sa grand-mere en face de la GH, il a des idees sur tout, passe du coq a l'ane en un clin d'oeil mais il est marrant. Nous allons diner au restau indien, of course. Ensuite, un des employes de la GH me propose un tour en discotheque, il m'embarque sur la motorbike (modele chinois a un prix derisoire) et nous voila dans une grande salle, seulement 3 tables occupees, ambiance mortellement ennuyeuse. Il est marie mais me demande au moins 10 fois si je veux une fille. Saoulant. Nous rentrons d'ailleurs a moitie stone, la route est glissante et il roule comme un dingue (autant que possible, a deux sur une mobylette chinoise et fatiguee).<p>

<h2>jeudi 7 aout</h2>
Trek sur le plateau Bolaven, avec Nathalie, instit' a Toulouse! Un guide pour deux touristes, c'est l'aubaine. Le temps est pourri, les chemins de terre glissent terriblement, on dirait du verglas. Le guide nous montre les cultures locales: arbre a cafe, the, pamplemousse. Nous avons droit ensuite a une vue magnifique sur des chutes d'eau qui seraient majestueuses, n'etait le brouillard dense qui arrete la vue a quelques dizaines de metres. Ensuite commence le trek proprement dit, jusque la c'etait de la ballade tranquille: terrain en pente franche, ca glisse tellement qu'on doit descendre (mais aussi monter - beaucoup plus dur) en rappel, accroche a des racines. Bref on est couverts de bous apres une demi-heure, le guide est ravi. Les sangsues attaquent, bref la jungle, la vraie. Nous visitons quelques villages qui repondent aux noms pittoresques de "kilometre 48" ou "kilometre 50". Les gamins sont ravis de nous voir, ils sont scotches par l'appareil photo numerique, ils peuvent se voir dedans! Le guide nous prepare un repas tout simple, tres apprecie sauf le dessert: du pamplemousse dans une sauce au sucre + sel + sauce au poisson. Special. Nous finissons la ballade la ou nous l'avons commencee, et cette fois le temps est degage: les chutes sont majestueuses, je confirme.<p>
Le soir nous allons manger un steak frites, l'idee est de Pierre.<p>

<h2>vendredi 8 aout</h2>
Direction Don Khong, dans un truck, nous sommes environ 20, plus des sacs de nourriture, dont un sac de grenouilles vivantes, et bien sur un poulet, qui evite d'attirer l'attention (bizarrement).<p>
