<h2>vendredi 29 aout</h2>

Je m'arrete dans une zone pleine d'hotels. Mais tous les batiments marques "hotel" sont en fait des restaurants! Je trouve quand meme une guest house confortable.<p>

<h2>samedi 30 aout</h2>

Je visite le fort Frederick, une presqu'ile avec un camp militaire et un temple hindou. La visite du temple se fait pieds nus, ce qui n'est pas une bonne idee car je dois marcher une cinquantaine de metres sur la route bouillante avant d'atteindre le sable salvateur, un poil moins brulant mais ca fait du bien. Je suis un "guide" sur les rochers, prends prends quelques photos des bateaux de pecheurs, quelques dizaines de metres en dessous, l'eau est transparente.<p>
Les Tamouls sont tres accueillants, tout sourire, les daims se promenent en liberte dans la ville (avec les vaches, mais je m'y suis habitue), les plages sont magnifiques et les bateaux de peche (sortes de prao) sont magnifiques. Mais ils aiment bien charger sur la decoration des qu'ils s'agit de religion. Bah, personne n'est parfait.<p>

En faisant reparer ma sandale par un cordonnier-du-bord-de-la-route, je discute avec un Francais, Michel, qui est parti en novembre et fait a peu pres le meme parcours que moi (a la sandale cassee pres!).<p>

Un tuk-tuk me fait faire la tournee des guest houses sur la plage d'Uppuveli, a 5 km de la. Je me decide pour French Garden, mais le nom n'a rien a voir. Le chauffeur de tuk-tuk me donne rendez-vous le matin suivant, je vais participer a la peche du matin.<p>

<h2>dimanche 31 aout</h2>

Le chauffeur de tuk-tuk vient me chercher a 7h15, je suis habille et 2 temps 3 mouvements, mais arrives dans la maison du pecheur on nous apprend qu'il est parti depuis 5h ce matin. Visite de la criee, le poisson est etale partout par terre, je dois faire attention ou je pose les pieds. Le chauffeur et moi allons chez un autre pecheur, la traduction simultanee anglais/tamoul est assuree par mon chauffeur. Je dois repasser ce soir a 17h30 pour une peche de nuit jusqu'a 5 heures demain matin. Youppieeee!! Youpie! youpi. Toute la journee j'y repense. Je vais passer 12 heures de peche a la ligne sur un bateau minuscule avec un type qui ne debloque pas un mot d'anglais. Il n'y aura personne d'autre en vue et probablement pas de musique. Bref mon enthousiasme refroidit a vue d'oeil.<p>

Je reviens au French Garden et je m'apprete a expliquer mon desistement au chauffeur de tuk-tuk, et surtout a affronter son enthousiasme indestructible. A 8h, toujours personne.<p>

<h2>lundi 1er septembre</h2>

Bus pour Kandy, arret a Dambulla comme d'habitude.<p>

