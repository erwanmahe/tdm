<h2>mardi 9 septembre</h2>

Nous arrivons enfin a Panaji, c'est le milieu de la matinee, je suis extenue. Je prends un bus local pour Calangute, a environ 1/2 heure de la.<p>
Des l'arrivee a Calangute, je remonte la plage, magnifique et deserte, vers Baga. J'y trouve une chambre pour une semaine, a 100 Rs/jour. 1er repas au bar-restaurant Athens sur la plage: du homard, on ne se refuse rien.<p>
Ici toutes les maisons, restaurants et GH ont un nom portugais, ca me rappelle le Bresil.<p>

<h2>mercredi 10 septembre</h2>

Je marche jusqu'a Anjuna, une plage un peu au nord de Baga, en longeant la cote (et les rochers). Petite pause dans un bar hippie, je savais bien qu'il en restait. Le retour est plus problematique, je decide de passer par la colline (hillside), et j'apercois plusieurs animaux: des paons, des faucons en vol stationnaire au-dessus des falaises. Mais je me perds dans le dedale de murets en pierre au sommet de la colline, je dois meme faire demi-tour parce que j'ai derange un petit groupe de buffle qui pataugeait dans une mare-baignoire. Je rentre par la foret, il fait deja presque nuit quand j'emerge enfin des buissons, Baga est droit devant. Ouf! Soiree Athens tres calme, passee a discuter avec Georges (le barman) de la vie de garcon de cafe.<p>

<h2>jeudi 11 septembre</h2>

Ballade dans Calangute, veritable petite ville comparee a la micro-Baga. Tout le monde se prepare a l'arrivee des touristes a la haute saison, qui commence dans un mois environ.<p>

<h2>vendredi 12 septembre</h2>

Je me decide a louer une mobylette et je pars en vadrouille du cote d'Anjuna et Vagator.<p>

<h2>samedi 13 septembre</h2>

Promenade jusqu'a Panaji. Je perds la mobylette dans un parking. Quel parking? Quelle rue? etc... Je passe l'heure suivante a vagabonder au hasard, comme je fais toujours dans une ville que je ne connais pas, et on finit par se retrouver avec la mobylette. Passees les effusions des retrouvailles, nous rentrons a Calangute et passons la soiree au Mambo.<p>

<h2>dimanche 14 septembre</h2>

Retour Panaji, j'arrive a la gare. Je commande un billet de train (c'est-a-dire que je remplis un petit papier avec mon nom, celui du train que je veux prendre, son numero, l'horaire, les gares de depart et de destination. A la fin, je temds un billet tellement rature au prepose que tout ce qu'il trouve a me dire est: "Full"<p>

Je me doute bien que le train n'est pas plein, mais il ne veut pas s'ennuyer a s'expliquer avec un touriste. Ici ils n'ont pas le petit bureau climatise special touristes comme a Chennai.<p>
Soiree a Calangute, la mobylette ne veut plus partir, je dois acheter un litre d'essence, dans une bouteille d'eau en plastique. Je trouve mon essence dans le dernier commerce ouvert a cette heure tardive. Il vend aussi des cigarettes et des allumettes, des briquets. Evidemment, tout est range au meme endroit.<p>

<h2>lundi 15 septembre</h2>

Quelques allers-retours a Calangute, je commande mon billet dans une agence de voyages<p>

<h2>mardi 16 septembre</h2>

Un taxi m'amene a la gare le matin, je pars pour Mumbay/Bombay.<p>
Je suis dans un compartiment sleeper (2eme classe, ca s'appelle sleeper car les sieges sont convertibles en couchettes), assez confortable, pas trop de monde 12 heures de trajet.<p>
