<h2>lundi 18 novembre</h2>
Le metro me transporte de l'aeroport a Tsim Sha Tsui, au sud de Kowloon. Direction K. Mansions, dixit le LP. K. Mansions est un haut lieu de l'immigration en provenance de tous les pays du Commconwealth. Je suis intercepte par une Chinoise qui me loue un placard pour 100HK$ la nuit. Mais c'est un placard de luxe, avec l'A/C, la tele et une salle de bains. C'est a peine plus cher que la chambre dans un dortoir du LP.<p>
Soiree dans les rues de Kowloon, tres animees et l'ambiance est plutot sympa.<p>
Le long de la promenade du HK Museum of Arts au sud de Tsim Sha Tsui, je prends quelques photos de l'ile de HK, juste en face. C'est la partie de HK ou se trouvent tous les hauts buildings. D'ici, le point de vue est impressionnant.<p>

<h2>mardi 19 novembre</h2>
Je prends le ferry direction l'ile de HK. . La traversee dure moins de 10 minutes, c'est un moyen de transport tres populaire, plus agreable que le metro.<p>
Je passe la soiree a Soho, le quartier branche de HK.<p>

<h2>mercredi 20 novembre</h2>
J'ai l'intention de passer la derniere journee a Macao, mais j'abandonne car c'est complique (nouvelle monnaie), trop long (la traversee prend quelques heures) et trop cher (navires hyper-motorises genre hydroglisseurs, tres chers).<p>
Retour sur l'ile, je monte au Sky Lobby de l'immeuble Plaza, au 47eme etage. La vue est magnifique.<p>
Promenade dans Soho de jour, occupee par les costumes-cravates. Visite du zoo, puis telepherique (peak tram) jusqu'au sommet du peak, une colline qui offre une vue splendide sur toute l'ile. J'entre dans un restaurant chinois (menu en chinois, serveur chinois, la seule chose que je puisse lire ce sont les numeros des plats. Mais je suis ici pour une raison precise. A peine entre je prononce les mots magiques: Dim Sun, le nom de la specialite locale, un assortiment de beignets cuits a la vapeur, c'est tres bon, je ne demande pas la composition des beignets.<p>
Je rentre a l'aeroport par le bus (deux etages, les seuls transports publics de HK a un seul etage sont les metros et le peak tram). Nous traversons deux tunnels et un pont suspendu. C'est moins rapide que le metro mais au moins il y a une vue.<p>

<h2>jeudi 21 novembre</h2>
A Johannesburg le matin, a Nairobi l'apres-midi, je suis la madonne des aeroports internationaux.<p>
