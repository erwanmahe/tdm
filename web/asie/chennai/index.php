<h2>jeudi 4 septembre</h2>

J'arrive a 9h du matin, je traverse tout le parking pour arriver a la station de train, direction Egmore (central, mais pas trop cher d'apres le Guide du Routard). Un type me repere et fait le guide, ce qui consiste a me preceder, donc a deviner ma direction, puis a me "presenter" les hotels indiques par le GR. Je trouve une chambre a 195 Rs (environ 4$). Comme il n'arrive pas a extorquer de commission a l'hotel, il se rabat sur moi mais n'a pas plus de chance, il y a des jours comme ca.<p>

La ville est extremement sale et dense, je vais me convertir a la nourriture vegetarienne, ca va me faire du bien. Par contre, pas un bar et pas d'alcool dans les restaurants: ca, ca va etre dur. La rue principale est Anna Dulai Street, je prends le train d'un quartier a l'autre: il y a des wagons "Ladies" beaucoup moins peuples que les wagons ordinaires. J'en use et en abuse (des wagons, bien sur). C'est pratique de parler francais quelquefois.

Je decouvre un interet cache de l'Inde: les reeditions "special Asie" de livres, a prix canon.<p>

<h2>vendredi 5 septembre</h2>

A la gare de Chennai Central, je prends un billet de train 1ere classe A/C dans l'express pour Bangalore. Ils ont un bureau climatise special touristes dans les gares. En fin d'apres-midi, je m'installe pour lire au bar du Taj Coromandel, salon cuir, lumiere tamisee et surtout de la biere fraiche. Ouf! J'etouffais dans la "foule-poussiere-bruit-odeur-chaleur-sueur-et meme des vaches"<p>

<h2>samedi 6 septembre</h2>

Le train est confortable, meme un peu froid, mais les fenetres teintees alternativement jaune/bleu me donnent l'impression de decouvrir les paysages de l'Inde dans le generique extremement long d'un film d'Almodovar.<p>

