<h2>lundi 11 novembre</h2>
J'arrive a Den Pasar, un taxi me depose a Kuta beach, le Kao Sarn de Bali. La plage est belle et le coucher de soleil somptueux. <p>

<h2>mardi 12 novembre</h2>
Location d'une mobylette, direction Ubud. Je me perds en route mais ce n'est pas grave, les paysages sont tellement beaux. Je trouve une GH sympa, pas trop chere, la mama decide de m'apprendre l'indonesien en deux jours. Je me jette sur mon guide de conversation toutes les deux secondes, et je dois declarer forfait apres deux minutes seulement.<p>
Visite d'Ubud en mobylette. L'atmosphere est beaucoup plus detendue qu'a Kuta, mais ca reste quand meme une ville touristique.<p>

<h2>mercredi 13 novembre</h2>
Re-ballade dans Ubud, cete fois a pied. La ville est pleine de temples.<p>

<h2>jeudi 14 novembre</h2>
J'enfourche la mob, direction les montagnes. Je suis arrete par l'orage au 4/5emes du chemin. Je dois m'abriter sous l'auvent d'une maison, ou un groupe de Balinaises preparent des salades de fruits pour au moins un regiment. Pendant ce temps, un convoi de plusieurs camions se dirige dans le temple juste en face,  avec a bord une armee de Balinais(es) en costume de ceremonie. Ils se protegent de la pluie avec des baches, mais il en faudrait plus pour entamer leur bonne humeur.<p>

<h2>vendredi 15 novembre</h2>
Re-ballade dans Ubud, et spectacle de danse balinaise le soir: "La Danse de la Grenouille". Le lieu du spectacle a ete modifie a cause de la pluie: d'un theatre en plein air, nous nous retrouvons dans une salle couverte.<p>
La danse de la grenouille, c'est l'histoire d'un prince qui va a la chasse, mais ne parvient a attraper que des libellules. Il les mange et Shiva qui passait par la trouve ca tellement degoutant qu'il transforme le prince en grenouille. La fiancee du prince, qui ne l'a jamais rencontre auparavant, n'a aucun mal a reconnaitre un prince dans la grenouille, mais le prince est malheureux, il veux retrouver son apparence normale. Shiva realise son erreur et rend au prince son apparence humaine. Mais la princesse ne reconnait plus le prince et se met a l'eviter. Heureusement Shiva arrive et resoud tous les problemes.<p>
La troupe se compose de 10 musiciens, une dizaines de danseurs et danseuses, plus une dizaine de grenouilles, plutot des enfants deguises en grenouilles. Et 4 spectateurs payants en tout: 2 japonaises qui encadrent une petite fille, et moi.<p>
Le spectacle est tres interessant, bien que destine a un jeune public. Au milieu du spectacle, je suis invite par le prince grenouille a me joindre a lui sur scene. Et la je fais la grenouille, ce qui consiste a le suivre partout sur la scene, en sautant a 4 pattes, et ce pendant 5 minutes. Je suis epuise. La petite fille du public est morte de rire et les deux Japonaises sont eperdues de reconnaissance.<p>

<h2>samedi 16 novembre</h2>
Retour Kuta, temps O.K. Quelques photos du paysage.<p>

<h2>dimanche 17 novembre</h2>
Matin a l'aeroport, tours et detours a cause des sens interdits et de la signalisation plus qu'aleatoire. Je m'envole demain pour HK.<p>
Comme le temps est magnifique, je decide d'aller a Amed, a l'autre bout de l'ile. Enfin au moins une partie du chemin, Amed doit etre a 3h de route au moins, j'ai prevu de m'arreter en route et de revenir.<p>
Grace a l'orage, c'est chose faite apres a peine un tiers de la route. Je suis bloque sur une plage de sable noir, jolis bateaux, quasiment personne, sauf un chien qui m'en veut pour une raison inconnue. 2-3 cocas plus tard, la pluie a diminue et je rentre vers Kuta. <p>

<h2>lundi 18 novembre</h2>
Un taxi me depose a l'aeroport, d'ou je decolle a 16h30.<p>
