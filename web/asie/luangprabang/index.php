<h2>mardi 29 juillet</h2>
Apres avoir trouve une guest house en bordure du Mekong, je marche dans la ville et me retrouve assez rapidement au marche: une seule rue, tres longue, avec des stands d'artisanat et de nourriture. Toute la rue vient d'etre plongee dans le noir, mais je vois desbougies s'allumer ca et la, et bientot le business reprend, au marche comme dans les restaurants, comme si de rien n'etait: ils sont bien habitues aux pannes de courant par ici.<p>

<h2>mercredi 30 juillet</h2>
Je me promene en ville, echange quelques livres. Je croise une famille de Francais qui etaient sur le bateau, ils me racontent qu'un bus a ete attaque sur la route 13, entre Luang Prabang et Vientiane.<p>
Je passe la soiree avec les Belges, le Kenian et les Anglaises.<p>

<h2>jeudi 31 juillet</h2>
Promenade en ville, visite des temples, quelques photos des batiments coloniaux. Au coucher du soleil, je monte sur Phnom ??, le point le plus haut de la ville, le coucher de soleil est, of course, magnifique.<p>

<h2>vendredi 1er aout</h2>
Le bus pour Vientiane part officiellement a 7h, en fait a 7h30. Nous traversons les montagnes, et plusieurs villages Hmongs. Ils sont tres pauvres, je vois regulierement des enfants porter de lourds fardeaux de bois. D'un autre cote, ils sont plutot heureux de leur sort. Personne n'a du leur expliquer qu'ils sont censes etre malheureux. Autour des villages, les poules, les chevres et les cochons se promenent en toute liberte. De temps en temps, au bord de la route, un militaire arme fait le guet. Ou ronfle.<p>
J'ai l'impression que le bus est le roi de la route. Le chauffeur pratique la conduite au klaxon, c'est assez simple:<br>
- une voiture ou une moto en face: je klaxonne<br>
- je veux doubler une voiture ou une moto: je klaxonne<br>
- un groupe de personnes ou d'animaux au bord de la route: je klaxonne<br>
- j'arrive a un virage ou au sommet d'une cote: je klaxonne<br>
- je traverse un village: je klaxonne<br>
Je me demande a quoi ressemble leurs examens du code de la route.<p>
Le paysage de montagnes dans les nuages est superbe. Le bus s'arrete a ??, la region des karsts. Et nous arrivons a Vientiane a 16h.<p>
