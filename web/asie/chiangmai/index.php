<h2>samedi 19 juillet</h2>
Une journee a Bangkok, le temps de me renseigner sur les horaires de train.
<h2>dimanche 20 juillet</h2>
Train a 7h30. Assez confortable, repas inclus, comme dans les avions. A cote de moi, Aung, une Thai avec qui j'arrive a avoir une conversation intelligente: elle est assez critique a propos de son pays, le "pays du sourire". Elle me presente ses deux amies, New et ??. Arrives a Chiang Mai en debut de soiree, nous montons dans un pickup et New et moi descendons apres 1 ou 2 km. Elle va me deposer dans le quartier des Guest Houses sur son scooter. La traversee de la ville est acrobatique, a cause du trafic et surtout de mon sac a dos enorme, force d'inertie non negligeable surtout lors des slaloms entre les voitures. Le scooter tangue un maximum mais je m'accroche et ca fonctionne. Une fois installe, je fais un tour, on est dimanche soir et c'est le jour du marche, tout le centre est envahi par des stands d'artisanat, de musique locale et de nourriture (locale aussi). Je me perds de place en place, impossible de se reperer dans les rues grouillantes de monde.<p>
<h2>lundi 21 juillet</h2>
Petite visite chez Super Panda Tour, je m'inscris pour un trek. Ensuite, je me promene dans a ville, le centre-ville est entoure par un canal qui forme un carre.<p>
<h2>mardi 22 juillet</h2>
C'est parti pour un <a href="http://www.erwan-adm.org/picture.php?album=asie/chiangmai/trek&picture=6&lang=fr">trek</a> de deux jours, en compagnie de 5 Americains et un couple d'Anglais. La ballade est excellente, d'autant que je n'ai pas fait de sport depuis longtemps. Nous visitons quelques villages Hmong et Karen. Finalement nous arrivons dans un petit village ou les guides nous preparent le diner, pendant que nous (les touristes) nous partageons un jackfruit: c'est super-gluant et ca ne part pas en lavant a l'eau. Le jackfruit est enorme, meme a 8 nous ne pouvons pas le finir. Nous lancons des morceaux aux animaux: apres 1 minute, nous sommes entoures par une menagerie de cochons, poules et meme un chien. Nat (l'Anglaise) finit par prendre le dernier quartier (enorme) du jackfruit et le balance au milieu des betes. C'est le chien le plus rapide: il s'en empare et disparait en courant avec sa prise, trop heureux.<p>
Le repas est tres varie, mais les guides mangent dans la piece a cote (la meme chose, mais beaucoup plus epice). Nous finissons la soiree autour du feu, bonne ambiance.<p>
<h2>mercredi 23 juillet</h2>
Nous sommes reveilles par les coqs a 4h30 (un coq insomniaque), 5h et 5h30 (le genre toujours en retard). Un petit trek, puis une ballade d'une heure <a href="http://www.erwan-adm.org/picture.php?album=asie/chiangmai/trek&picture=18&lang=fr">a dos d'elephant</a>. Le guide est a pied et encourage l'elephant avec des ordres secs. Et aussi un lance-pierres de temps en temps: je ne sais pas ou il vise, mais c'est diablement efficace. Les Anglais nous quittent juste apres avec un des deux guides car ils ont pris le trek de 3 jours. Les Americains et moi allons dejeuner pres d'une cascade. Heureusement il y a des petites cabanes, car il se met a pleuvoir copieusement. Peu apres, nous nous retrouvons dans un taxi pour la riviere, pour une descente <a href="http://www.erwan-adm.org/picture.php?album=asie/chiangmai/trek&picture=24&lang=fr">"bamboo rafting"</a>: rien de vraiment dangereux excitant, c'est un peu comme faire de la barque au bord de la Marne, le cote aventure en moins. Mais on rigole bien quand meme. Nous rentrons enfin, trempes mais ravis, a Chiang Mai ou nous nous separons.<p>
Je prends un bus local (le numero 13) pour Chiang Rai, qui met 4 heures et s'arrete partout.<p>
