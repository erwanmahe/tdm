<h2>mardi 16 septembre</h2>

Des l'arrivee, j'appelle l'Apollo Guest House, un taxi me depose a l'Apollo GH, mais ce n'est pas la meme! Il faut drolement se mefier, ici. Heureusement, la "vraie" Apollo GH n'est pas loin.<p>

<h2>mercredi 17 au vendredi 19 septembre</h2>

Temps magnifique, Mumbay est immense, surpeuplee, mais plus propre que Chennai. J'y etouffe (un peu) moins. L'architecture de la ville est sauvee par quelques batiments datant de l'epoque anglaise, dont la gare centrale, superbe.<p>

<h2>samedi 20 et dimanche 21 septembre</h2>

Derniere visite de Mumbay, achat de livres et depart pour Varanasi le soir. Le train est surpeuple, je suis en sur-reservation. Je finis par partager ma banquette avec un vieux a qui on a vendu une reservation portant le meme numero. Autant dire que je passe le plus clair de mon temps assis ou couche par terre. L'enfer va durer 34 heures.<p>
