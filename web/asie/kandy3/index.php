<h2>lundi 1er septembre</h2>

Je prends une chambre dans une guest house sur la colline au-dessus du lac, la vue est magnifique.<p>

<h2>mardi 2 septembre</h2>

Vers 9 heures, je retrouve Michel a l'ambassade, il pleut. Hanneke arrive un peu plus tard. Nos visas en poche, nous repartons vers le centre ville non sans avoir dument photographie l'hote du jour de l'ambassade: un serpent. Petit dejeuner au Devon. Je fais visiter le jardin botanique a Hanneke qui s'y ennuie profondem,ent. Il y a quelque chose que je n'ai pas du faire dans l'ordre. Chacun vaque a ses occupations et je retrouve toute la troupe le soir, il y a aussi Pablo, un Anglais, la soiree est animee.<p>

<h2>mercredi 3 septembre</h2>

Reveil 13h, argh! J'emmene un tuk-tuk pour le circuit banque-gare-guest house. Je suis dans l'Observation Wagon (l'equivalent de la 1ere classe) du train de 15h. 170 roupies, c'est donne.<p>

Vus sur la route de Kandy a Colombo: des carcasses de wagons renverses au bord de la voie, des herons, de tunnels, la jungle. Adieu le Sri Lanka.<p>

A Colombo, je dine au seafish, excellent comme d'habitude. Une file d'attente immense devant le cinema. Chambre au ex-servicemen's institute, juste a cote du YMCA mais beaucoup plus propre.<p>

<h2>jeudi 4 septembre</h2>

Lever en pleine nuit, puis une heure de taxi, et j'arrive enfin a l'aeroport a 5h45, soit exactement 2 heures avant mon vol: 7h45. Et j'ai bien fait car aujourd'hui il est avance a 6h50. Bref je suis en retard. Le vol dure 1 heure, bref pas vraiment le temps d'apprecier le service du leader mondial des compagnies aeriennes.<p>
