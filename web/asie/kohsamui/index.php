<h2>mercredi 28 mai</h2>
Le bus de nuit me depose a Surat Thani, ou j'attends le bateau en compagnie de Marissa, une Francaise qui va a Koh Phan gahn (meme bateau, mais un peu plus loin que Koh Samui). Pendant la traversee, une Thai joviale fait le tour des touristes en proposant ses bungalows a 100 Baht. Le temps de reflechir, et je me retrouve dans un petit bungalow a 100 Baht, avec ventilo et moustiquaire, sur la plage. Pendant le repas, je retrouve un groupe de 4 Canadiens qui etaient sur le bateau.<p>
Je fais un tour a Lamai, la ville la plus proche, et je fais la connaissance d'un Suedois, bien bourre, le soir. Les Canadiens rentrent peu apres, l'un d'eux a un pansement sur le front: ils ont rencontre un de leurs "amis" dans un bar, qui a pete les plombs et fracasse une bouteille sur le crane du Canadien. Ils rentraient de l'hopital quand je les ai vus.<p>
<h2>jeudi 29 mai</h2>
Je me baigne sur la plage en evitant les cailloux, et je decide que j'ai mieux a faire sur terre. Donc je loue une mobylette, et je passe l'apres-midi a Chaweng, la grande ville de l'ile. J'y visite une galerie de peinture, ou les tableaux sont ranges par genres: imitations de Dali, de Picasso, de Warhol, etc...<p>
Le soir, partie de cartes le soir avec Suzanna (Suede), Laura (GB) et un couple d'Allemands (?). C'est marrant ca, je ne me rappelle que des 2 filles.<p>
<h2>vendredi 30 mai</h2>
Viree a Chaweng avec Suzanna et le Suedois d'avant-hier, plus un de ses potes, Suedois aussi. Bref, nous allons dans un bar Suedois, ecouter des chansons suedoises. C'est a peu pres aussi fin que le top 50 allemand, mais tout le monde s'amuse. Une petite Thai vient nous vendre des colliers de fleurs, et elle revient 3,4 fois, au final tout le monde craque. Bien sur il y a d'autres enfants qui viennent nous vendre des colliers, mais la moitie du bar porte ses fleurs rouges. Je suis admiratif.<p>
Nous embarquons dans un taxi, un pick-up 4x4 bache, avec des loupiotes de toutes les couleurs, pour la "black moon party". L'ile voisine de Koh Phan Gahn etant mondialement celebre pour la fameuse "full moon party", Koh Samui a decide d'occuper le creneau de toutes les autres phases de la lune, avec la "Black moon party", la "Half moon party", et je suis sur egalement la "Two third moon", la "Full moon was yesterday", la "Black moon minus two days", etc...<p>
Dans le taxi, il y a un Americain, completement bouure (il a encore un verre a la main), il s'emporte contre le monde entier, surtout la France, car on est en pleine guerre du Golfe 2. Suzanne et moi passons notre temps a nous engueuler avec lui, et nous arrivons a la Black Moon tres enerves, loin de l'esprit peace and love de rigueur. Bref nous entrons sans payer, sans un regard pour le type avec les billets. Malheureusement, l'ambiance est glauque sur la plage, malgre la decoration sympa, la house degoulinante fait fuir meme les plus motives d'entre nous, et nous rentrons nous coucher en ordre disperse.<p>
<h2>samedi 31 mai</h2>
Ce soir, nous allons a un match de boxe thai gratuit sur la place du marche de Lamai. Je retrouverai les autres pendant le match, parce que j'ai decide en secret de m'offrir un super-repas: le surf and turf, c'est-a-dire 3 tiger prawns et un steak bien epais. Je retrouve les Suedois (accompagnes de Laura et du couple d'Allemands) qui s'ennuient ferme autour du match, et nous decidons d'aller a Chaweng (encore) ou nous rencontrons un Anglais qui possede un bar sur l'ile (en fait, a 300m de nos bungalows). Il ouvre le bar, il est passe minuit et le quartier est desert. Les serveuses qui dormaient derriere le comptoir sont reveillees et l'alcool coule a flots.<p>
<h2> dimanche 1er juin</h2>
Je n'ai meme pas le temps de dire au revoir, je prends le bateau pour Surat Thani a midi, pendant que nous attendons le bus pour Bangkok, des travellers utilisent leur telephone portable: autant pour le depaysement.<p>
