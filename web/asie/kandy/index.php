<h2>samedi 23 aout</h2>
Nous visitons le plus vieux temple du Sri Lanka, je suis impressionne. A l'entree, je pose mes sandales, puis je suis fouille, et mon sac a dos est entierement vide par un policier zele. A l'interieur du temple, les accessoires de l'Esala Perahera, plus grande fete religieuse du Sri Lanka, avec des processions d'elephants carapaconnes et des dizaines de danseurs, que j'ai ratee a dix jours pres. Je ne suis jamais la ou il faut les jours de pleine lune, apparemment. Je prends un guide qui me fait faire le tour des salles, le receptacle de la dent sacree de Bouddha, puis la salle ou sont rassembles des Bouddhas de tous les pays. Ensuite, je peux enfin poser mon sac au "Olde Empire Hotel", tres sympa et pas cher. Juste a cote du temple, le lac (au centre de la ville) n'est pas loin, bref c'est un joli quartier.<p>

<h2>dimanche 24 aout</h2>
Nous visitons le jardin botanique, superbe, anglais. Des arbres geants qui s'etalent sur le sol, dont les branches sont soutenues par des madriers, une serre d'orchidees, une serre sans lumiere pour une collection de plantes ombrophiles(?), et surtout ce qui m'a plu: des milliers de chauve-souris suspendues aux arbres la tete en bas. Nous sortons trop tard du jardin, nous allons rater le train pour Nurawa Eliya: un tuk-tuk nous depose a la station suivante: 600 roupies (6$ argh!).<p>
Il n'y a plus de places assises dans le train. Avec l'aide de Michael, j'arrive a me trouver a une place de choix, assis dans la porte du wagon, celle qui donne sur la voie, les deux pieds dehors et l'appareil-photo a la main. Le voyage est extremement lent, mais le petit diesel cheminant entre deux collines couvertes de plantations de the, facon colegiala (mais avec du the, donc), je m'en rappellerai un bout de temps.<p>
