<h2>samedi 6 septembre</h2>

Je prends un rickshaw pour M.G. Road (M.G.=Mahatmah Ghandhi, quand on a compris ca, on a la moitie des rues de l'Inde en poche). Il commence a me faire la visite des monuments de la ville. Il m'enerve. "M.G. Road NOW!" L'hotel du GR est complet, les autres hotels trop chers. Je me dirige vers la YMCA, un grand batiment tres colonial et tres tres anglais, avec des terrains de sport, qui est complet aussi. On m'indique un autre hotel pas loin, plus cher mais grand luxe (pour moi, en tous cas: tele, serviette et salle de bains, et le tout tres propre).<p>

Debarrasse de mon sac a dos, je retourne me promener a M.G. Road et Brigade Rd, les deux rues "occidentales" de la ville. Galleries marchandes, Nike, PS2, Mac Donalds, tout y est. Ils ont meme quelques blancs expatries pour faire plus vrai. Je delaisse mon vieux GR edition 2000 pour le LP India 2003.<p>

<h2>dimanche 7 septembre</h2>

