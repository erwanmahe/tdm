<h2>mercredi 14 mai</h2>
J'arrive le soir, toutes les AJ sont fermees, je finis par telephoner au "Serpent", qui se trouve a 30 mn a pied du centre. Ils m'envoient une navette. Pendant l'attente, une vieille aborigene rode autour de l'arret de bus, apparemment stone. Je lui propose 2AUS$, pour l'aider. "Later" me dit-elle. Comprends pas. Mais tous les aborigenes que je verrai a Cairns ont l'air defonces en permanence. C'est triste de les voir convertis en attractions touristiques par les autorites locales.<p>
<h2>jeudi 15 mai</h2>
Journee absolument pas productive, je me ballade en ville, je me promene et je passe la soiree dans un bar.
<h2>vendredi 16 - samedi 17 mai</h2>
Journee snorkelling sur le reef (corail). Nous sommes sur un bateau (que j'ai failli rater, la encore - decidemment) avec 5 membres d'equipage et...7 touristes. Le bateau s'arrete 2 fois, le 1er site est une "ile aux oiseaux", on n'a pas le droit d'y accoster et les oiseaux le savent bien: ils sont des centaines a se serrer sur une bande d'herbe et de sable qui affleure a peine au-dessus du niveau de la mer. Le bateau s'arrete (sans je ter l'ancre, bien sur), et les snorkellers sont deposes sur l'ile. 2 d'entre nous decident de plonger mais je prefere ne pas prendre trop de risque avec mes oreilles, d'autant que je vais prendre l'avion tres bientot. Donc, j'enfile mon masque, mon tuba et mes palmes, et je me glisse dans l'eau a reculons.<p>
Une fois a l'interieur, c'est genial. Les poissons, coraux de toutes les couleurs, les benitiers (gros coquillages). Je n'entends rien sauf un "cric-croc" persistant, je me demande ce que c'est jusqu'a ce que j'apercoive un poisson-perroquet (ainsi nomme a cause de son bec) qui grignote du corail. C'est lui qui fait tout ce bruit! Par moment je dois rentrer le ventre car j'ai l'impression que je passe a qq centimetres a peine au-dessus des coraux. En fait le masque a un effet de loupe assez puissant, pour m'en convaincre je regarde ma main de temps en temps. La premiere fois j'ai pousse un hurlement d'horreur.<p>
Le 2eme stop est different, les coraux sont organises en colonnes vertigineuses, j'ai l'impression de voler. Ici les poissons sont enormes, avec ou sans masque. Au final, une superbe journee et de beaux souvenirs. Je ne me doute pas encore que le souvenir le plus marquant est imprime sur mon dos, grace au soleil australien garanti 100% UV, et a la mince couche d'eau qui agit comme une loupe.<p>
J'ai demenage dans une AJ en centre-ville, je vais voir "Matrix reloaded" au cinema avec un roommate Suisse francophone. Puis nous allons au club 5-9, une boite techno sympa. Bref je ne dors pas de la nuit, je rentre juste a l'AJ pour recuperer mon sac, puis je prends un taxi pour l'aeroport, ou je m'assoupis sur un siege en attendant le decollage. Je me reveille par miracle 5 minutes avant l'heure du decollage, j'ai failli rater l'avion - question d'habitude.<p>
