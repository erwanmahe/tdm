<h2>mercredi 30 avril</h2>
Arrivee tot le matin a l'aeroport d'Auckland. Je suis accueilli par le 3eme age, des volontaires me disent-ils. Ils sont tres sympas, m'offrent le the, l'aeroport est hyper-clean et moderne. Je passe un peu de temps a lire "stupid white men" de Michael Moore en attendant les 1ers bus pour la city.<p>
Je descends au ACB (Auckland Central Backpackers) au 3eme etage d'un immeuble sur Queen Street, l'artere principale. En fait, tout l'immeuble est un hotel pour backpackers! Le 3eme etage est reserve a l'accueil, l'administration, une agence de voyage, une salle internet, des salles tele, cuisine, etc... Bref le backpacker de l'age industriel.<p>
Selon ma routine habituelle, je visite la ville a pied, les rues sont assez abruptes pour peu qu'on essaie de quitter Queen St. Je fais le tour du port, des immeubles ultra-modernes, je suis frappe par les boutiques et restaurants de toutes les communautes asiatiques possibles et imaginables. D'ailleurs plusieurs boutiques sont couvertes d'annonces entierement en chinois. Pas facile pour reperer les promotions. Je ne resiste pas aux sachimis, tempura, servis accompagnes de 5 ou 6 bols contenant du chou, de la soupe, des pousses de soja, des patates douces ou des sauces. Bref on mange bien, varie et copieux.<p>
Le temps est splendide. Je prends un ticket pour visiter la baie, nous sommes moins de 10 touristes a partager le pont du bateau, largement la place pour se deplacer d'un bord a l'autre et prendre la meilleure photo. Nous passons sous le Harbour Bridge, puis rentrons dans le port de l'America's Cup, ou on ne voit aucun bateau de course mais des yachts de croisiere immenses, voiliers monocoques avec un seul mat (ca a un nom, mais je ne m'en rappelle plus), bref des monstres. J'ai lu plus tard que certains d'entre eux font jusqu'a 40 metres. Fou, non?<p>
De retour sur terre, je reprends mon exploration, la nuit tombe. Je n'ai pas faim, mais je rentre presque malgre moi dans un restaurant, encore du poisson, et avec un verre de vin blanc NZ, cette fois. Suit ue petite visite au bar de l'ACB, vaste et anime mais je ne suis pas dans etat d'esprit potache-braillard-biere-billard. Je crois que je ne serai plus jamais dans cet etat d'esprit, grand bien me fasse.<p>

<h2>jeudi 1er mai</h2>
Re-ballade dans la ville, aujourd'hui j'essaie les gateaux, tout est bon.<p>
Ca n'a rien a voir, mais la circulation aux croisements est tres stricte. D'abord, ce sont les voitures qui tournent a droite (les NZ roulent a gauche) dans une direction puis l'autre. Ensuite viennent les voitures qui se deplacent en ligne, dans 1 direction, puis l'autre. Enfin, c'est au tour des pietons qui peuvent se deplacer en ligne ou en diagonale.<p>
Apres 5 minutes, je me lasse de ce fascinant spectacle et je prends un billet pour la Sky Tower. C'est vraiment une tour "pour le fun", comme la tour Eiffel: seul le rez-de-chausse est utilise par les restaurants, salles de spectacles, etc...Cela dit, la nuit, ca fait un cone de lumiere bleue du plus bel effet, tellement 21eme siecle!<p>
En haut, il n'y a rien sauf les jumelles et le magasin de souvenirs. On peut marcher sur des plaques de plexiglas, on a l'impression d'avoir les pieds dans le vide. Meme si je savais que ces vitres sont "aussi resistantes que du beton, vous pouvez y aller!", je m'y deplace extremement prudemment.<p>
Puis le bus m'emmene a Tororua, a travers le paysage vallone de la NZ, baignee dans la lumiere de fin d'apres-midi (tadammm).<p>
Et c'est plein de moutons. Pour de vrai.<p>
