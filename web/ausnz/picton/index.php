<h2>mardi 6 mai</h2>
Je debarque du ferry, il est environ 10 heures d soir et je suis transi de froid. Je m'engouffre dans le 1er mini-bus pour une AJ. 2 minutes apres, le mini-bus parcourt exactement 100m et nous debarque dans une grande maison, "family-run" et le chauffeur du mini-bus se transforme en maitre de maison. Pas de clefs sur la porte des chambres. Je reste discuter avec un Anglais qui travaille ici en attendant de se refaire. Il a beaucoup voyage, c'est une mine d'informations sur l'Afrique.<p>
<h2>mercredi 7 mai</h2>
Promenade a pied le long des sounds, temps superbe, vue imprenable: j'apercois le ferry qui s'engage dans le sound a des kilometres de la.<p>
<h2>jeudi 8 mai</h2>
Je prends un ticket pour une croisiere sur le sound, dans un petit bateau a moteur. Nous sommes 4 touristes, alors qu'il y a de la place pour 20. Le pilote nous explique la vegetation, nous montre les oiseaux et les otaries. Le tour s'arrete juste a temps pour que je puisse attraper le ferry pour Wellington, d'ou je monte dans le train de nuit pour Auckland.<p>
<h2>vendredi 9 mai</h2>
Visite a Qantas, mon avion decolle demain.<p>
<h2>samedi 10 mai</h2>
Chose promise chose due: je m'envole pour Melbourne.<p>




Reveil tot, un petit tour sur le port endormi. J'ai pris un ticket pour Picton, sur l'ile sud, en fin d'apres-midi (pour la lumiere).<p>
Un petit malentendu avec un chauffeur de bus, et me voila transporte a l'autre bout de la baie. Je devrais etre contrarie, mais la vue est superbe, et je n'ai pas d'emploi du temps, alors...<p>
Retour a Wellington pour prendre le ferry, qui appareille juste pour le coucher de soleil, pour une traversee de 3 heures.<p>
