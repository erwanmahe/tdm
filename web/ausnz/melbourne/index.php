<h2>samedi 10 mai</h2>
Avion pour Melbourne a 6AM. J'arrive dans l'AJ en compagnie de Bianca, une allemande fort sympathique, mais tres timide. Nous nous inscrivons pour une visite de la ville, les quartiers, grec, italien, gypsi. Le guide est genial, super-enthousiaste. Ca ne le derange pas du tout qu'on soit les 2 seules personnes du bus.
Le soir, je fais un tour dans le bar de l'AJ, en compagnie de mes "roommates", je rentre avant tout le monde et je suis reveille a 4 heures du matin par une bande avinee et braillarde, qui marche sur tous les dechets eparpilles dans la chambre.<p>
<h2>dimanche 11 mai</h2>
Je change d'AJ
Bianca et moi faisons un petit tour au marche, ou nous faisons le plein de fruits, et de tout ce qui nous a semble bon. Bref on est surcharges. Nous nous trainons jusqu'a la cuisine de l'AJ. Enfin son AJ, puisque je viens d'en changer. Personne ne me remarque, tout va bien. La cuisine est delicieuse, merci Bianca. Qui vit dans un village, possede des chevaux, bref une fille simple. Un petit tour en ville, visite du casino, le coucher de soleil (obligatoire!) sur Melbourne et nous nous separons.<p>
