<h2>jeudi 1er mai</h2>
Nous arrivons juste apres la fermeture de l'office de tourisme. J'emprunte le LP d'un autre backpacker et me dirige resolument vers le "Crash Palace", uniquement pour le nom. Resolument, c'est-a-dire dans la direction opposee. Apres quelques detours et moult demandes d'explications aux locaux, j'arrive enfin au CP, sympathique, "family-run" comme ils disent. Le soir je me promene en ville, je passe a l'"Outlaw" ou l'on peut voir a travers une vitre dans le bar, sans descendre de sa chaise (c'est une selle de cheval) et sans quitter son demi, des fous sportifs s'entrainer sur un mur d'escalade.<p>
<h2>vendredi 2 mai</h2>
Promenade autour du lac, la condensation (ou les sources chaudes) donne a la foret tout autour un air mysterieux. Ou fantomatique. De toutes facons, il se met a pleuvoir, je rentre au CP pour lire un peu. Je vais rester la toute la journee, tant la pluie n'arrete pas de redoubler. En passant devant le "Convention Centre", je prends un billet pour le "Pink Floyd Experience" le soir meme.Ce n'est pas un concert mais plutot une comedie musicale ou les acteurs-musiciens "rejouent" le film sur scene. Mais je viens d'apprendre que "The Wall" etait une comedie musicale avant d'etre un film. En tous cas, ce n'est pas tres convaincant et les NZ pensent comme moi de toutes facons.<p>

<h2>samedi 3 mai</h2>
Aujourd'hui il fait beau, j'achete un billet de bus pour Taupo. Je me promene dans la petite ville proprette. Les parcs sont tres bien entretenus, et decores par des statues Maori. Je tombe en arret devant un BYO indien. En face, un liquor shop. Bref, la combinaison parfaite, je ne peux pas rater ca. Apres un tour au liquor shop, je dejeune d'un butter lamb accompagne d'une 1/2 bouteille de rouge NZ, delicieux. Evidemment, tout ca m'a mis en retard pour mon bus, je prends le suivant en fin de journee. Nous sommes 3 dans un bus pour 45 personnes. Et les routes sont toutes neuves. Et il n'y a quasiment pas de trafic. Bref ca me change de l'Amerique du Sud et de Cuba. Et c'est reparti pour la lumiere de fin d'apres-midi, mais les paysages semblent etre de plus en plus beaux a mesure que je descends dans le sud.<p>
