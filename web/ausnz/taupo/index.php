<h2>samedi 3 mai</h2>
Nous arrivons sur Taupo, superbe vue sur le lac, entre chien et loup. Le ciel, pas le lac. Je prends une chambre a la YHA.<p>
J'essaie de sortir (apres tout, on est samedi), mais l'ambiance "far ouest" me rafraichit un peu.<p>
<h2>dimanche 4 mai</h2>
Sur les conseils des residents de la YHA, je prends le "Hot bus" qui fait le tour des attractions locales. Il me depose aux 'craters of the moon", une zone geothermale peu etendue mais tres active: geysers d'eau, odeurs d'acide, bouillonnement de boue, bref ce sont les terres noires de Tolkien. Le hot bus me recupere sur le chemin du retour, et me depose 5 mn aux Haku Falls, jolies.<p>
Et je prends le bus pour Wellington, ou nous arrivons sous la pluie, alors que le voyage avait fort bien commence, dans la campagne moutonneus baignee dans la lumiere de fin d'apres-midi que je commence a connaitre par coeur. Et je debarque au YHA central, meme principe que le ACB: massif, professionnel, efficace.<p>
